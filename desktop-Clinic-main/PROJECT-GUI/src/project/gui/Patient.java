    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int row =tablepatient.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(this,"NO row is selected ! please select on row ","Select row",
                JOptionPane.ERROR_MESSAGE);

        }else{
            DefaultTableModel model =(DefaultTableModel) tablepatient.getModel();
            String username=model.getValueAt(tablepatient.getSelectedRow(), 0).toString();
            
            try{
                CallableStatement stmt = connection.prepareCall("{call deletepatient(?)}");
                stmt.setString(1,username);
                stmt.executeQuery();
                
            }catch(Exception e){
                System.out.println("e");
            }
            model.removeRow(row);

        }

    }//GEN-LAST:event_btndeleteActionPerformed

    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        int row=tablepatient.getRowCount();
        int col=tablepatient.getColumnCount();
        

        
         DefaultTableModel tbModel=(DefaultTableModel) tablepatient.getModel();
        for(int i=0;i<row;i++){
            String[] update=new String[6];
            for(int j=0;j<col;j++){
                update[j]=tbModel.getValueAt(i, j).toString();
         
            }
            
            try{
                
            CallableStatement stmt = connection.prepareCall("{call editpatient(?,?,?,?,?,?)}");
            stmt.setString(1,update[0]);
            stmt.setString(2,update[1]);
            stmt.setString(3,update[2]);
            stmt.setString(4,update[3]);
            stmt.setString(5,update[4]);
            stmt.setString(6,update[5]);
            
             stmt.executeQuery();
             
        
        
        }catch(Exception e){
            System.out.println(e);
        }
            
            
            
            
            
        }

        
        Admin admin=new Admin();
        admin.show();
        dispose();
    }//GEN-LAST:event_formWindowClosed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        txtdoctor.removeAllItems();
        try{
            
             CallableStatement stmt = connection.prepareCall("{call selectalldoctor}");
              ResultSet rs =stmt.executeQuery();
              
              
              
              
              
              while(rs.next()){
                  String username=rs.getString("fullName");
                  if(!username.equalsIgnoreCase("Admin") ){
                  txtdoctor.addItem(username);
                  }    
             }
             
              
              
              
            CallableStatement stm = connection.prepareCall("{call tablepatient}");
            ResultSet rse =stm.executeQuery();
            while(rse.next()){
            String username=rse.getString("fullName");
            String exam=rse.getString("examination");
            String phone=rse.getString("phone");
            String history=rse.getString("history");
            String age=rse.getString("age");
            String doctor=rse.getString("doctor");
           

            String tbData[]={username,exam,phone,doctor,history,age };
            DefaultTableModel tbModel=(DefaultTableModel) tablepatient.getModel();
            tbModel.addRow(tbData);
            
            }
              
              
        }catch(Exception e){
            System.out.println(e);
        }
        
        
        
        
        
    }//GEN-LAST:event_formWindowOpened

