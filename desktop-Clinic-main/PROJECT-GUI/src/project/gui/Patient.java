    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        String username=txtusername.getText();
        String exam=txtexam.getSelectedItem().toString();
        String phone=txtphone.getText();
        String doctor=txtdoctor.getSelectedItem().toString();
        String history=txthistory.getText();
        String age=txtage.getText();
        
        if (username.isEmpty() || phone.isEmpty() || age.isEmpty() || exam.isEmpty() ||doctor.isEmpty() ){
            JOptionPane.showMessageDialog(this,"please enter all fields","try again",
                JOptionPane.ERROR_MESSAGE);

        }else{
            DefaultTableModel model =(DefaultTableModel) tablepatient.getModel();
            model.addRow(new Object[]{username,exam,phone,doctor,history,age});
            txtusername.setText("");
            txtphone.setText("");
            txthistory.setText("");
            txtage.setText("");
            txtdoctor.setToolTipText("");
            
            try{
                
            CallableStatement stmt = connection.prepareCall("{call insertpatient(?,?,?, ?,?,?)}");
            stmt.setString(1,username);
            stmt.setString(2,phone);
            stmt.setString(3,doctor);
            stmt.setString(4,history);
            stmt.setString(5,age);
            stmt.setString(6,exam);
        
            
            stmt.executeQuery();
            }catch(Exception e){
                System.out.println(e);
                
            }
            
            

        }
    }//GEN-LAST:event_btnaddActionPerformed

