    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int row =tablepatient.getSelectedRow();
        if (row <0){
            JOptionPane.showMessageDialog(this,"No row is selection! please select one row",
                    "selection row",
                   JOptionPane.ERROR_MESSAGE );
        }else{
            DefaultTableModel model=(DefaultTableModel) tablepatient.getModel();
//            select of patient name
             String username=model.getValueAt(tablepatient.getSelectedRow(), 0).toString();
             
             String history=model.getValueAt(tablepatient.getSelectedRow(), 2).toString();
             
             try{
                CallableStatement stmt = connection.prepareCall("{call updatepatient(?,?)}");
                stmt.setString(1,username);
                 stmt.setString(2,history);
                
                stmt.executeQuery();
                
            }catch(Exception e){
                System.out.println("e");
            }
            model.removeRow(row);
        }
    }//GEN-LAST:event_btndeleteActionPerformed

