    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
     
        String username=lbldoctor.getText();
//        System.out.println(username);
         try{
              CallableStatement stmt = connection.prepareCall("{call selectpatient(?)}");
            stmt.setString(1,username);
            ResultSet rs=stmt.executeQuery();
            
            while(rs.next()){
                String use=rs.getString("fullName");
                String examination=rs.getString("examination");
                String history=rs.getString("history");
                String age=rs.getString("age");
                String doctor=rs.getString("doctor");
//                System.out.println("bad");
                if(doctor.equalsIgnoreCase(username)&& examination.equalsIgnoreCase("Yes")){
//                    System.out.println("good");
                    String tbData[]={use,age,history };
                    DefaultTableModel tbModel=(DefaultTableModel) tablepatient.getModel();
                    tbModel.addRow(tbData);
                
                }
                
            }
         }catch(Exception e){
             
         }
         
    }//GEN-LAST:event_formWindowOpened

